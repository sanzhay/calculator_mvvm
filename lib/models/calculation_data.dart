class CalculationData {
  double result = 0.0;
  double n1 = 0;
  double n2 = 0;
  String op = '';

  double getResult() {
    return result;
  }

  void setResult(double result) {
    this.result = result;
  }

  double getN1() {
    return n1;
  }

  void setN1(num1) {
    this.n1 = num1;
  }

  double getN2() {
    return n2;
  }

  void setN2(num2) {
    this.n2 = num2;
  }

  String getOperator() {
    return op;
  }

  void setOperator(operator) {
    this.op = operator;
  }
}
